async function init() {
  try {
    await webcam.setup();
  } catch (e) {
    document.getElementById('no-webcam').style.display = 'block';
  }
}

async function webcamPredict(){
  predict(webcam.capture());
}

// Initialize the application.
init();
document.getElementById('predictor').onclick =  webcamPredict;


